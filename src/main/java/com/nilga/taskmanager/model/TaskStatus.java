package com.nilga.taskmanager.model;

/**
 * Enumeration representing the possible statuses of a task in the Task Manager application.
 */
public enum TaskStatus {
    /**
     * Indicates a newly created task that has not yet been started.
     */
    NEW,

    /**
     * Indicates a task that is currently in progress.
     */
    IN_PROGRESS,

    /**
     * Indicates a task that has been completed.
     */
    COMPLETED
}
